package com.github.matteo099.opencv;

public class ShapePoint {
    public int x;
    public int y;

    public ShapePoint() {

    }

    public ShapePoint(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
